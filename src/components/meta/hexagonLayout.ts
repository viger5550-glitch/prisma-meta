/**
 * Раскладка шести карточек по вершинам правильного шестиугольника, вписанного в окружность радиуса R.
 *
 * Центр композиции: (0, 0) в локальных координатах секции; на экране совмещается с центром контейнера.
 * Каждая вершина лежит на расстоянии R от центра; соседние вершины разнесены по азимуту на 60°.
 *
 * Азимут θ отсчитывается от оси +X к оси +Y (как в Math.cos/sin в CSS-плоскости: X вправо, Y вниз).
 * Тогда верхняя точка на вертикали «вверх» от центра — это θ = −90°.
 *
 * Вершины (против часовой стрелки от верха):
 *   −90° — верх, −30° — правая верхняя, 30° — правая нижняя, 90° — низ,
 *   150° — левая нижняя, 210° — левая верхняя.
 *
 * Координаты центра карточки относительно центра композиции:
 *   x = R * cos(θ),  y = R * sin(θ)   (θ в радианах).
 *
 * Ориентация карточек: вершины верх/низ (−90°, 90°) без поворота (0°). Остальные — базово
 * rotation = angle + 90°, затем нормализация читаемости: если rotation ∈ (90°, 270°), вычитаем 180°,
 * чтобы текст не был вверх ногами (верх текста наружу композиции).
 *
 * Центральный круг «Об авторе» не поворачивается (rotation = 0°), подпись горизонтальная.
 */
export const HEX_AZIMUTHS_DEG = [-90, -30, 30, 90, 150, 210] as const;

export type HexAzimuthDeg = (typeof HEX_AZIMUTHS_DEG)[number];

/** Радиус окружности через вершины шестиугольника (пиксели); центр карточки лежит на окружности. */
export function hexPixelOffsets(
  azimuthDeg: HexAzimuthDeg,
  radiusPx: number,
): { x: number; y: number } {
  const rad = (azimuthDeg * Math.PI) / 180;
  return {
    x: radiusPx * Math.cos(rad),
    y: radiusPx * Math.sin(rad),
  };
}

/**
 * Радиальный наклон с коррекцией читаемости (без перевёрнутого текста).
 */
export function cardRotationDegForAzimuth(azimuthDeg: HexAzimuthDeg): number {
  if (azimuthDeg === -90 || azimuthDeg === 90) {
    return 0;
  }

  let rotation = azimuthDeg + 90;
  if (rotation > 90 && rotation < 270) {
    rotation -= 180;
  }
  return rotation;
}
